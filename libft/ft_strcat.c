#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	len_s1;
	size_t	len_s2;

	len_s1 = ft_strlen(s1);
	len_s2 = ft_strlen(s2);
	ft_memcpy(s1 + len_s1, s2, len_s2);
	s1[len_s1 + len_s2] = '\0';
	return (s1);
}
